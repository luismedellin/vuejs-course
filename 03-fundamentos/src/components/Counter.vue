<template>
  <h2>Counter</h2>
  <p>{{ counter }} <sup>2</sup> = {{ squareCounter }}</p>
  <button @click="decrease">-1</button>
  <button @click="update(-1)">-1</button>
  <button @click="increase">+1</button>
  <button @click="update(+1)">+1</button>
</template>

<script>
export default {
  data() {
    return {
      counter: 5
    }
  },
  methods: {
    getSqureValue() {
      return this.counter * this.counter
    },
    increase() {
      this.counter++;
    },
    decrease() {
      this.counter--;
    },
    update(value) {
      this.counter += value
    }
  },
  computed: {
    squareCounter() {
      return this.counter*this.counter
    }
  }
}
</script>

